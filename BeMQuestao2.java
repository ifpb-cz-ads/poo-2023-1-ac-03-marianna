public class BeMQuestao2 {
    public static void main(String[] args){
         int i = 1;
        System.out.println("i = " + i);

        long j = 2L;
        System.out.println("j = " + j);

        float p = 20.0f;
        System.out.println("p = " + p);

        double q = 30.0;
        System.out.println("q = " + q);

        boolean b = true;
        System.out.println("b = " + b);

        char c = 'k';
        System.out.println("c = " + c);
    }
}
